package com.example.quizstar.Model;

public class Balance {
    String status, balance, kels;
    Balance data;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getBalance() {
        return balance;
    }

    public void setBalance(String balance) {
        this.balance = balance;
    }

    public String getKels() {
        return kels;
    }

    public void setKels(String kels) {
        this.kels = kels;
    }

    public Balance getData() {
        return data;
    }

    public void setData(Balance data) {
        this.data = data;
    }
}
